package org.wgq.idt.info;

public class DownloadInfo {

	private String value;
	
	public DownloadInfo(String value) {
		this.value = value;
	}
	
	public void setValue(String value) {
		this.value = value;
	}
	
	public String getValue() {
		return this.value;
	}
	
	public String toString() {
		return this.value;
	}
}
